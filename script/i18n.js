const htmlLang = {
  es: "es",
  pt: "pt-BR",
  en: "en"
};

function getInitialLanguage() {
  const savedLang = localStorage.getItem("portfolioLang");

  if (savedLang && translations[savedLang]) {
    return savedLang;
  }

  const browserLang = navigator.language || navigator.userLanguage;

  if (browserLang.startsWith("pt")) return "pt";
  if (browserLang.startsWith("en")) return "en";

  return "es";
}

function setLanguage(lang) {
  const selectedLanguage = translations[lang] ? lang : "es";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const value = translations[selectedLanguage][key];

    if (value) {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    const value = translations[selectedLanguage][key];

    if (value) {
      element.setAttribute("placeholder", value);
    }
  });

  document.documentElement.lang = htmlLang[selectedLanguage];
  localStorage.setItem("portfolioLang", selectedLanguage);

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === selectedLanguage);
  });

  const cvFiles = {
      es: {
        href: "./assets/cv/CV_Lihuel_Muracciole_ES.pdf",
        download: "CV_Lihuel_Muracciole_ES.pdf"
      },
      pt: {
        href: "./assets/cv/CV_Lihuel_Muracciole_PT.pdf",
        download: "CV_Lihuel_Muracciole_PT.pdf"
      },
      en: {
        href: "./assets/cv/CV_Lihuel_Muracciole_EN.pdf",
        download: "CV_Lihuel_Muracciole_EN.pdf"
      }
    };

    document.querySelectorAll(".cv-download").forEach((link) => {
      link.setAttribute("href", cvFiles[selectedLanguage].href);
      link.setAttribute("download", cvFiles[selectedLanguage].download);
    });
}

document.addEventListener("DOMContentLoaded", () => {
  const initialLang = getInitialLanguage();

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => {
      setLanguage(btn.dataset.lang);
    });
  });

  setLanguage(initialLang);
});